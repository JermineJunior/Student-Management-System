<template>
 <div
        ref="scrollTopButton"
        class="sticky w-full flex justify-end bottom-0 pb-3 pr-5 transition"
    >
        <div
            class="text-gray-400 hover:text-blue-400 transition"
        >
            <button  @click="scrollToTop"   role="button"
                aria-label="scroll to top of the page">
               <svg  class="size-12" viewBox="-2.5 -2.5 30.00 30.00" fill="none" xmlns="http://www.w3.org/2000/svg"
               stroke="#68a6f5">
               <g id="SVGRepo_bgCarrier" stroke-width="0"></g>
               <g id="SVGRepo_tracerCarrier" stroke-linecap="round" stroke-linejoin="round" stroke="#68a6f5" stroke-width="0.05"></g><g id="SVGRepo_iconCarrier"> <path d="M12 22.4199C17.5228 22.4199 22 17.9428 22 12.4199C22 6.89707 17.5228 2.41992 12 2.41992C6.47715 2.41992 2 6.89707 2 12.4199C2 17.9428 6.47715 22.4199 12 22.4199Z" stroke="#68a6f7" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round"></path> <path d="M8 13.8599L10.87 10.8C11.0125 10.6416 11.1868 10.5149 11.3815 10.4282C11.5761 10.3415 11.7869 10.2966 12 10.2966C12.2131 10.2966 12.4239 10.3415 12.6185 10.4282C12.8132 10.5149 12.9875 10.6416 13.13 10.8L16 13.8599" stroke="#68a6f6" stroke-width="1.5" stroke-linecap="round" stroke-linejoin="round">

               </path> </g></svg>
            </button>
        </div>
    </div>
</template>

<script setup>
  import { onMounted, onUnmounted, ref } from "vue";
  import debounce from 'lodash.debounce';
  let scrollTopButton = ref(null);
  let handleDebouncedScroll = ref(null);
  const handleScroll = () => {
    if (window.scrollY > 0) {
      scrollTopButton.value.classList.remove("invisible");
    } else {
      scrollTopButton.value.classList.add("invisible");
    }
  };
  const scrollToTop =  () =>  {
    window.scrollTo({ top: 0, behavior: "smooth" });
 }
  onMounted(() => {
    handleDebouncedScroll = debounce(handleScroll, 150);
    window.addEventListener('scroll', handleDebouncedScroll);
  });

  onUnmounted(() => {
      // Remove listener from the debounced method instead:
    window.removeEventListener('scroll', handleDebouncedScroll);
  });
</script>
